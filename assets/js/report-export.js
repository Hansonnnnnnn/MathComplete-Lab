/*
  MathComplete Lab - Practice Report Export

  Adds a report download button to practice result screens and exports the
  current round as a standalone HTML report.
*/

(function () {
  const ROUND_KEY_PREFIX = "mcl_report_round:";
  const MAX_ATTEMPTS = 200;
  let attempts = [];
  let roundStartedAt = new Date().toISOString();
  let originalRecordGameAttempt = null;
  let originalRecordAttempt = null;
  let buttonObserver = null;

  function lang() {
    return localStorage.getItem("mathcomplete_lang") === "zh" ? "zh" : "en";
  }

  function copy() {
    return lang() === "zh"
      ? {
          download: "下载成绩报告",
          title: "练习成绩报告",
          summary: "成绩概览",
          details: "逐题记录",
          mistakes: "错题整理",
          generated: "由 MathComplete Lab 生成",
          tool: "练习工具",
          date: "生成时间",
          started: "开始时间",
          finished: "结束时间",
          mode: "答题模式",
          difficulty: "难度",
          total: "题目数量",
          correct: "正确题数",
          accuracy: "正确率",
          wrong: "错题数量",
          question: "题目",
          yourAnswer: "你的答案",
          correctAnswer: "正确答案",
          result: "结果",
          correctTag: "正确",
          wrongTag: "错误",
          blank: "未作答",
          noMistakes: "本轮没有错题。",
          noAttempts: "本轮还没有可导出的答题记录。",
          printTip: "如需 PDF，可在浏览器中使用打印并选择“保存为 PDF”。"
        }
      : {
          download: "Download Report",
          title: "Practice Report",
          summary: "Summary",
          details: "Question Details",
          mistakes: "Mistake Review",
          generated: "Generated by MathComplete Lab",
          tool: "Practice Tool",
          date: "Generated",
          started: "Started",
          finished: "Finished",
          mode: "Practice Mode",
          difficulty: "Difficulty",
          total: "Questions",
          correct: "Correct",
          accuracy: "Accuracy",
          wrong: "Wrong",
          question: "Question",
          yourAnswer: "Your answer",
          correctAnswer: "Correct answer",
          result: "Result",
          correctTag: "Correct",
          wrongTag: "Incorrect",
          blank: "No answer",
          noMistakes: "No wrong answers this round.",
          noAttempts: "There are no answers to export yet.",
          printTip: "To make a PDF, open the report and use your browser's Print / Save as PDF option."
        };
  }

  function safe(value) {
    if (value === null || value === undefined) return "";
    return String(value);
  }

  function escapeHtml(value) {
    return safe(value)
      .replaceAll("&", "&amp;")
      .replaceAll("<", "&lt;")
      .replaceAll(">", "&gt;")
      .replaceAll('"', "&quot;")
      .replaceAll("'", "&#039;");
  }

  function attr(value) {
    return escapeHtml(value).replaceAll("\n", " ");
  }

  function stripHtml(value) {
    const tmp = document.createElement("div");
    tmp.innerHTML = safe(value);
    return tmp.textContent || tmp.innerText || "";
  }

  function formatDate(value) {
    try {
      return new Intl.DateTimeFormat(lang() === "zh" ? "zh-CN" : "en-US", {
        year: "numeric",
        month: "short",
        day: "2-digit",
        hour: "2-digit",
        minute: "2-digit"
      }).format(new Date(value));
    } catch {
      return safe(value);
    }
  }

  function slug(value) {
    return safe(value)
      .toLowerCase()
      .replace(/[^a-z0-9\u4e00-\u9fa5]+/g, "-")
      .replace(/^-+|-+$/g, "")
      .slice(0, 60) || "mathcomplete-report";
  }

  function currentGameId() {
    const explicit = window.MCLQuizTool?.gameId;
    if (explicit) return explicit;
    const file = window.location.pathname.split("/").pop() || "practice.html";
    return file.replace(/\.html$/i, "");
  }

  function currentToolTitle() {
    return (
      document.getElementById("pageTitle")?.textContent?.trim() ||
      document.querySelector("h1")?.textContent?.trim() ||
      document.title ||
      currentGameId()
    );
  }

  function currentModeLabel() {
    const select = document.getElementById("toolMode");
    if (select?.selectedOptions?.[0]) return select.selectedOptions[0].textContent.trim();
    return window.MCLToolModes?.getMode?.() || "";
  }

  function currentDifficultyLabel() {
    const select = document.getElementById("difficulty");
    if (select?.selectedOptions?.[0]) return select.selectedOptions[0].textContent.trim();
    return document.getElementById("difficultyText")?.textContent?.trim() || "";
  }

  function roundStorageKey() {
    return `${ROUND_KEY_PREFIX}${currentGameId()}`;
  }

  function saveRound() {
    try {
      sessionStorage.setItem(roundStorageKey(), JSON.stringify({ roundStartedAt, attempts: attempts.slice(-MAX_ATTEMPTS) }));
    } catch {
      // Ignore storage failures; in-memory export still works.
    }
  }

  function loadRound() {
    try {
      const raw = sessionStorage.getItem(roundStorageKey());
      const parsed = raw ? JSON.parse(raw) : null;
      if (Array.isArray(parsed?.attempts)) {
        attempts = parsed.attempts;
        roundStartedAt = parsed.roundStartedAt || roundStartedAt;
      }
    } catch {
      attempts = [];
    }
  }

  function resetRound() {
    attempts = [];
    roundStartedAt = new Date().toISOString();
    saveRound();
  }

  function questionTextFrom(question, payload) {
    return safe(
      payload?.questionLatex ||
      payload?.question_latex ||
      question?.main ||
      question?.displayExpression ||
      question?.latex ||
      question?.equationText ||
      question?.plain ||
      payload?.questionText ||
      payload?.question_text ||
      question?.expression ||
      ""
    );
  }

  function plainQuestionFrom(question, payload) {
    return safe(
      payload?.questionText ||
      payload?.question_text ||
      question?.plain ||
      question?.equationText ||
      stripHtml(question?.main || question?.displayExpression || question?.latex || "")
    );
  }

  function answerText(value) {
    if (value && typeof value === "object") {
      return safe(value.latex ?? value.text ?? value.value ?? "");
    }
    return safe(value);
  }

  function normalizeAttempt(payload = {}, rawPayload = null) {
    const question = payload.question || payload.questionSnapshot || rawPayload?.question || {};
    const selected = payload.timeUp
      ? ""
      : (payload.selectedAnswer ?? payload.selectedAnswerLatex ?? payload.selected_answer_latex ?? payload.selected_answer ?? "");
    const correct = payload.correctAnswer ?? payload.correctAnswerLatex ?? payload.correct_answer_latex ?? payload.correct_answer ?? "";

    return {
      index: attempts.length + 1,
      gameId: safe(payload.gameId || payload.game_id || currentGameId()),
      toolTitle: currentToolTitle(),
      topic: safe(payload.topic || question.type || question.templateId || question.builder || ""),
      difficulty: safe(payload.difficulty || question.difficulty || document.getElementById("difficultyText")?.textContent?.trim() || ""),
      questionLatex: questionTextFrom(question, payload),
      questionText: plainQuestionFrom(question, payload),
      selectedAnswerLatex: answerText(selected),
      correctAnswerLatex: answerText(correct),
      isCorrect: Boolean(payload.isCorrect ?? payload.is_correct),
      timeUp: Boolean(payload.timeUp),
      answeredAt: payload.answeredAt || new Date().toISOString(),
      timeSpentSeconds: Number.isFinite(Number(payload.timeSpentSeconds ?? payload.time_spent_seconds))
        ? Math.max(0, Math.round(Number(payload.timeSpentSeconds ?? payload.time_spent_seconds)))
        : null
    };
  }

  function rememberAttempt(payload, rawPayload = null) {
    const item = normalizeAttempt(payload, rawPayload);
    if (!item.questionLatex && !item.questionText) return;
    attempts.push(item);
    attempts = attempts.slice(-MAX_ATTEMPTS);
    saveRound();
  }

  function patchProgress() {
    const progress = window.MCLProgress;
    if (!progress || progress.__mclReportPatched) return false;

    if (typeof progress.recordGameAttempt === "function") {
      originalRecordGameAttempt = progress.recordGameAttempt.bind(progress);
      progress.recordGameAttempt = function (payload) {
        rememberAttempt(payload);
        return originalRecordGameAttempt(payload);
      };
    }

    if (typeof progress.recordAttempt === "function") {
      originalRecordAttempt = progress.recordAttempt.bind(progress);
      progress.recordAttempt = function (payload) {
        rememberAttempt(payload);
        return originalRecordAttempt(payload);
      };
    }

    progress.__mclReportPatched = true;
    return true;
  }

  function stats() {
    const total = attempts.length;
    const correct = attempts.filter(item => item.isCorrect).length;
    const wrong = total - correct;
    const accuracy = total ? Math.round((correct / total) * 100) : 0;
    return { total, correct, wrong, accuracy };
  }

  function renderMath(value) {
    const text = safe(value);
    if (!text) return `<span class="blank">${escapeHtml(copy().blank)}</span>`;
    return `<span class="math" data-latex="${attr(text)}">${escapeHtml(text)}</span>`;
  }

  function reportHtml() {
    const t = copy();
    const s = stats();
    const finishedAt = new Date().toISOString();
    const wrongItems = attempts.filter(item => !item.isCorrect);
    const tool = currentToolTitle();
    const mode = currentModeLabel();
    const difficulty = currentDifficultyLabel();

    return `<!DOCTYPE html>
<html lang="${lang() === "zh" ? "zh-CN" : "en"}">
<head>
  <meta charset="UTF-8" />
  <meta name="viewport" content="width=device-width, initial-scale=1.0" />
  <title>${escapeHtml(t.title)} | ${escapeHtml(tool)}</title>
  <link rel="stylesheet" href="https://cdn.jsdelivr.net/npm/katex@0.16.9/dist/katex.min.css">
  <style>
    :root { color-scheme: light; --ink:#0f172a; --muted:#64748b; --line:#e2e8f0; --blue:#2563eb; --good:#047857; --bad:#b91c1c; }
    * { box-sizing: border-box; }
    body { margin:0; font-family: ui-sans-serif, system-ui, -apple-system, BlinkMacSystemFont, "Segoe UI", "Microsoft YaHei", Arial, sans-serif; color:var(--ink); background:#f8fafc; }
    main { width:min(980px, calc(100% - 28px)); margin:0 auto; padding:32px 0 44px; }
    header { padding:26px 0 20px; border-bottom:1px solid var(--line); }
    h1 { margin:0; font-size:clamp(30px,5vw,48px); line-height:1.08; letter-spacing:0; }
    h2 { margin:28px 0 12px; font-size:24px; letter-spacing:0; }
    p { color:var(--muted); line-height:1.65; margin:8px 0 0; }
    .meta { display:grid; grid-template-columns:repeat(2,minmax(0,1fr)); gap:10px 18px; margin-top:18px; color:var(--muted); font-weight:750; }
    .meta span strong { color:var(--ink); }
    .summary { display:grid; grid-template-columns:repeat(4,minmax(0,1fr)); gap:12px; margin-top:14px; }
    .stat { background:#fff; border:1px solid var(--line); border-radius:14px; padding:16px; }
    .num { display:block; font-size:30px; font-weight:950; color:var(--blue); }
    .label { color:var(--muted); font-weight:850; }
    .item { background:#fff; border:1px solid var(--line); border-radius:16px; padding:16px; margin:12px 0; break-inside:avoid; }
    .item-head { display:flex; justify-content:space-between; gap:12px; flex-wrap:wrap; margin-bottom:10px; color:var(--muted); font-weight:850; }
    .ok { color:var(--good); font-weight:950; }
    .bad { color:var(--bad); font-weight:950; }
    .qa { display:grid; gap:8px; line-height:1.7; }
    .qa strong { color:var(--ink); }
    .blank { color:var(--muted); font-style:italic; }
    .math { overflow-wrap:anywhere; }
    footer { color:var(--muted); border-top:1px solid var(--line); margin-top:30px; padding-top:16px; font-weight:800; }
    @media (max-width:720px) { .summary,.meta { grid-template-columns:1fr; } }
    @media print { body { background:#fff; } main { width:100%; padding:18px; } .item,.stat { box-shadow:none; } }
  </style>
</head>
<body>
  <main>
    <header>
      <h1>${escapeHtml(t.title)}</h1>
      <p>${escapeHtml(tool)}</p>
      <div class="meta">
        <span><strong>${escapeHtml(t.tool)}:</strong> ${escapeHtml(tool)}</span>
        <span><strong>${escapeHtml(t.mode)}:</strong> ${escapeHtml(mode || "-")}</span>
        <span><strong>${escapeHtml(t.difficulty)}:</strong> ${escapeHtml(difficulty || "-")}</span>
        <span><strong>${escapeHtml(t.date)}:</strong> ${escapeHtml(formatDate(finishedAt))}</span>
        <span><strong>${escapeHtml(t.started)}:</strong> ${escapeHtml(formatDate(roundStartedAt))}</span>
        <span><strong>${escapeHtml(t.finished)}:</strong> ${escapeHtml(formatDate(finishedAt))}</span>
      </div>
    </header>
    <section>
      <h2>${escapeHtml(t.summary)}</h2>
      <div class="summary">
        <div class="stat"><span class="num">${s.total}</span><span class="label">${escapeHtml(t.total)}</span></div>
        <div class="stat"><span class="num">${s.correct}</span><span class="label">${escapeHtml(t.correct)}</span></div>
        <div class="stat"><span class="num">${s.accuracy}%</span><span class="label">${escapeHtml(t.accuracy)}</span></div>
        <div class="stat"><span class="num">${s.wrong}</span><span class="label">${escapeHtml(t.wrong)}</span></div>
      </div>
    </section>
    <section>
      <h2>${escapeHtml(t.details)}</h2>
      ${attempts.map(item => reportItemHtml(item, t)).join("")}
    </section>
    <section>
      <h2>${escapeHtml(t.mistakes)}</h2>
      ${wrongItems.length ? wrongItems.map(item => reportItemHtml(item, t)).join("") : `<p>${escapeHtml(t.noMistakes)}</p>`}
    </section>
    <footer>
      <div>${escapeHtml(t.generated)}</div>
      <p>${escapeHtml(t.printTip)}</p>
    </footer>
  </main>
  <script src="https://cdn.jsdelivr.net/npm/katex@0.16.9/dist/katex.min.js"><\/script>
  <script>
    document.querySelectorAll("[data-latex]").forEach(el => {
      try { katex.render(el.dataset.latex, el, { throwOnError:false, displayMode:false }); }
      catch { el.textContent = el.dataset.latex || ""; }
    });
  <\/script>
</body>
</html>`;
  }

  function reportItemHtml(item, t) {
    const resultClass = item.isCorrect ? "ok" : "bad";
    const resultText = item.isCorrect ? t.correctTag : t.wrongTag;
    return `<article class="item">
      <div class="item-head">
        <span>#${item.index}${item.topic ? ` · ${escapeHtml(item.topic)}` : ""}</span>
        <span class="${resultClass}">${escapeHtml(resultText)}</span>
      </div>
      <div class="qa">
        <div><strong>${escapeHtml(t.question)}:</strong> ${renderMath(item.questionLatex || item.questionText)}</div>
        <div><strong>${escapeHtml(t.yourAnswer)}:</strong> ${renderMath(item.selectedAnswerLatex)}</div>
        <div><strong>${escapeHtml(t.correctAnswer)}:</strong> ${renderMath(item.correctAnswerLatex)}</div>
      </div>
    </article>`;
  }

  function downloadReport() {
    loadRound();
    if (!attempts.length) {
      alert(copy().noAttempts);
      return;
    }

    const html = reportHtml();
    const blob = new Blob([html], { type: "text/html;charset=utf-8" });
    const url = URL.createObjectURL(blob);
    const a = document.createElement("a");
    const date = new Date().toISOString().slice(0, 10);
    a.href = url;
    a.download = `${slug(currentToolTitle())}-report-${date}.html`;
    document.body.appendChild(a);
    a.click();
    a.remove();
    window.setTimeout(() => URL.revokeObjectURL(url), 1000);
  }

  function addButton() {
    const resultCard = document.getElementById("resultCard");
    if (!resultCard || resultCard.classList.contains("hidden")) return;
    if (resultCard.querySelector("[data-report-download]")) return;

    const actions = resultCard.querySelector(".actions") || resultCard;
    const button = document.createElement("button");
    button.type = "button";
    button.className = "ghost";
    button.dataset.reportDownload = "true";
    button.textContent = copy().download;
    button.addEventListener("click", downloadReport);
    actions.appendChild(button);
  }

  function refreshButtonText() {
    document.querySelectorAll("[data-report-download]").forEach(btn => {
      btn.textContent = copy().download;
    });
  }

  function observeResultCard() {
    const resultCard = document.getElementById("resultCard");
    if (!resultCard || buttonObserver) return;
    buttonObserver = new MutationObserver(addButton);
    buttonObserver.observe(resultCard, { attributes: true, attributeFilter: ["class"], childList: true, subtree: true });
    addButton();
  }

  function bindRoundReset() {
    document.addEventListener("click", event => {
      if (event.target.closest("#startBtn, #restartBtn, #reviewWrongBtn")) {
        resetRound();
      }
    }, true);
  }

  function init() {
    loadRound();
    bindRoundReset();
    observeResultCard();
    patchProgress();
    window.addEventListener("storage", event => {
      if (event.key === "mathcomplete_lang") refreshButtonText();
    });
  }

  if (document.readyState === "loading") document.addEventListener("DOMContentLoaded", init);
  else init();

  window.MCLReportExport = {
    downloadReport,
    resetRound,
    getAttempts: () => [...attempts],
    addButton
  };
})();
